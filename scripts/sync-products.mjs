import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const ROOT = path.join(__dirname, '..')
const SOURCE = 'C:\\Users\\Dell\\Desktop\\Sukiri Products'
const DEST = path.join(ROOT, 'public', 'products')
const OUT = path.join(ROOT, 'src', 'data', 'shopItems.js')

function toSlug(name) {
  return name
    .replace(/\.[^.]+$/, '')
    .replace(/[^a-zA-Z0-9]+/g, '-')
    .replace(/^-|-$/g, '')
    .toLowerCase()
}

function parseProduct(sourceFile) {
  const base = sourceFile.replace(/\.[^.]+$/, '')
  const ext = path.extname(sourceFile).toLowerCase()
  const priceMatch = base.match(/\(Rs\s*(\d+)(?:\s+each)?\)/i)

  let priceValue = priceMatch ? Number(priceMatch[1]) : null
  let name = base
    .replace(/\(Rs\s*\d+(?:\s+each)?\)/gi, '')
    .replace(/\s*\(\d+\)\s*$/, '')
    .trim()

  const lower = name.toLowerCase()
  if (priceValue === null && lower.includes('shroomies')) priceValue = 550

  const whatsappOverrides = {
    'whatsapp image 2026-06-29 at 9.17.19 pm': {
      name: 'Cotton Candy Tulip Bouquet',
      priceValue: 3000,
      category: 'Bouquets',
    },
    'whatsapp image 2026-06-29 at 9.17.20 pm': {
      name: 'Shroomies',
      priceValue: 550,
      category: 'Keychains',
    },
    'whatsapp image 2026-06-29 at 9.17.23 pm': {
      name: 'Blush Tulip Trio Bouquet',
      priceValue: 2000,
      category: 'Bouquets',
    },
  }

  const override = whatsappOverrides[lower]
  if (override) {
    name = override.name
    priceValue = override.priceValue
  }

  let category = override?.category ?? 'Accessories'
  if (!override) {
    if (lower.includes('keychain') || lower.includes('shroomies')) category = 'Keychains'
    else if (lower.includes('bouquet')) category = 'Bouquets'
    else if (lower.includes('plush') || lower.includes('penguin')) category = 'Plushies'
  }

  const destFile = `${toSlug(sourceFile)}${ext}`

  return { name, category, priceValue, destFile, sourceFile }
}

function formatPrice(n) {
  return `Rs. ${n.toLocaleString('en-PK')}`
}

if (!fs.existsSync(SOURCE)) {
  console.warn(`Source folder not found (${SOURCE}) — skipping product sync.`)
  process.exit(0)
}

fs.mkdirSync(DEST, { recursive: true })

const sources = fs
  .readdirSync(SOURCE)
  .filter((f) => /\.(jpe?g|png|webp)$/i.test(f))
  .sort((a, b) => a.localeCompare(b))

const existing = new Set(fs.readdirSync(DEST))
for (const file of existing) {
  if (!sources.some((s) => toSlug(s) + path.extname(s).toLowerCase() === file)) {
    fs.unlinkSync(path.join(DEST, file))
  }
}

const products = sources.map((sourceFile, index) => {
  const parsed = parseProduct(sourceFile)
  fs.copyFileSync(path.join(SOURCE, sourceFile), path.join(DEST, parsed.destFile))

  return {
    id: index + 1,
    name: parsed.name,
    category: parsed.category,
    price: parsed.priceValue ? formatPrice(parsed.priceValue) : 'Price on request',
    priceValue: parsed.priceValue,
    image: `/products/${parsed.destFile}`,
    description: '✿ handmade with love — made to order just for you',
  }
})

const customId = products.length + 1
const output = `// Auto-generated by scripts/sync-products.mjs — run: npm run sync-products

export const shopItems = ${JSON.stringify(
  [
    ...products,
    {
      id: customId,
      name: 'Custom Order',
      category: 'Custom',
      price: 'Price on request',
      priceValue: null,
      image: null,
      description: "Have something in mind? Let's make it.",
    },
  ],
  null,
  2,
)}

export const categories = ['All', 'Keychains', 'Plushies', 'Bouquets', 'Custom']
`

fs.writeFileSync(OUT, output)
console.log(`Synced ${products.length} products from Sukiri Products folder.`)
